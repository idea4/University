package Testss;

public class GeneralForLoop {

	public static void main(String[] args) {
		
		
		
			
		
		int doWhileCounter;
		doWhileCounter =2;
		
		do {
			System.out.println(doWhileCounter);  /// will always run once 
			doWhileCounter++;
		} while(doWhileCounter <10);

	}

}
